package Ej1;

public class Ej1App {

	public static void main(String[] args) {
		
		//Declaración de variables
		int num1 = 23;
		int num2 = 12;
		
		//Prints de las operaciones
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println((float)num1 / num2); //Cast del resultado de la división para que se vea el valor decimal
		System.out.println(num1 % num2);

	}

}
